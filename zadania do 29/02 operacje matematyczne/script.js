let wynik2 = skladnik1 / skladnik2;
document.write(skladnik1 + "/" + skladnik2 + "=" + wynik2 );
if (skladnik2 === 0){
    Document.write("Nie dzielimy przez 0")
} else

let wynik3 = skladnik1 - skladnik2;
document.write(skladnik1 + "-" + skladnik2 + "=" + wynik3 );

